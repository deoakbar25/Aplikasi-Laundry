    private void load_table(){
        // membuat tampilan model tabel
        DefaultTableModel model = new DefaultTableModel();
        
        model.addColumn("ID");
        model.addColumn("Jenis Cucian");
        model.addColumn("Harga Per Kilo");
        model.addColumn("Berat");
        model.addColumn("Total Harga");
        model.addColumn("Bayar");
        jTable1.setModel(model);
        
        
        //menampilkan data database kedalam tabel
        try {
         res=stm.executeQuery("Select * FROM transaksi");
         while(res.next()){
             model.addRow(new Object[]{
             res.getString("id_laundry"),
             res.getString("jenis_cucian"),
             res.getString("harga_perkilo"),
             res.getString("berat"),
             res.getString("total_harga"),
             res.getString("bayar"),
             });
         }
         }catch(Exception e){
             JOptionPane.showMessageDialog(null, "gagal menampilkan data"+e.getMessage());
         }
}
    
