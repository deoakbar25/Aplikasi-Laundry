    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       try {
            String sql = "INSERT INTO `transaksi` (`jenis_cucian`, `harga_perkilo`, `berat`, `total_harga`, `bayar`) VALUES ('"+
                    jenis_cuci.getSelectedItem()+"','"+
                    Harga.getText()+"','"+
                    Berat.getText()+"','"+
                    Ttl_Harga.getText()+"','"+
                    Bayar.getText()+"')";
            java.sql.Connection conn=(Connection)koneksi.configDB();
            java.sql.PreparedStatement pst=conn.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Penyimpanan Data Berhasil");
            load_table();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        
        int bayar = Integer.parseInt(Bayar.getText());
        int ttl_harga = Integer.parseInt(Ttl_Harga.getText());
        double kembalian = bayar - ttl_harga;
        String kembali = Double.toString(kembalian);
        JOptionPane.showMessageDialog(null,"Kembalian Anda : \n"+kembali);
        
        jenis_cuci.setSelectedItem(jenis_cuci);
        Harga.setText("");
        Berat.setText("");
        Ttl_Harga.setText("");
        Bayar.setText("");
        
    }//GEN-LAST:event_jButton2ActionPerformed

